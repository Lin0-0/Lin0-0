let isAnimating = false;

person.addEventListener('click', () => {
  if (isAnimating) return;
  isAnimating = true;

  // 模擬三次鞠躬
  let count = 0;
  const bow = () => {
    person.style.transform = 'translateX(-50%) rotateX(10deg)';
    setTimeout(() => {
      person.style.transform = 'translateX(-50%) rotateX(0deg)';
      count++;
      if (count < 3) {
        setTimeout(bow, 500);
      } else {
        // 顯示光芒
        light.style.opacity = 1;
        setTimeout(() => {
          // 顯示籤詩
          const randomIndex = Math.floor(Math.random() * quotes.length);
          quoteText.textContent = quotes[randomIndex];
          quoteBox.style.display = 'block';
        }, 1000);
      }
    }, 500);
  };
  bow();
});
